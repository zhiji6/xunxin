public class UserAccessLinkedRecordVO extends GeneralBean{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String  							linked_url;			//链接
	private String  							title;				//标题
	private String  							source;				//来源
	private String  							content;			//文章内容
	private String  							image;				//图片
	private int  								userId;				//用户
	
	
	public String getLinked_url() {
		return linked_url;
	}
	public void setLinked_url(String linked_url) {
		this.linked_url = linked_url;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	
	
	

}
