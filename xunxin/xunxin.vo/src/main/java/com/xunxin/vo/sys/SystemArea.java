public class SystemArea {
	
	public SystemArea() {
		super();
	}
	
	public SystemArea(String code, String name, String type) {
		super();
		this.code = code;
		this.name = name;
		this.type = type;
	}

	private int id;
	private String code;
	private String name;
	private String type;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
	
	
}
