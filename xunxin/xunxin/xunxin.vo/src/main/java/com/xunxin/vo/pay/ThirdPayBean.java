public class ThirdPayBean {

	private int id;
	private int bank_id;		//银行id
	private String third_name;	//第三方支付名称
	private String third_type;	//第三方支付类型
	private String mer_no;		//商户号
	private String mer_key;		//商户秘钥(公|私)
	private String return_url;	//异步通知地址
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getBank_id() {
		return bank_id;
	}
	public void setBank_id(int bank_id) {
		this.bank_id = bank_id;
	}
	public String getThird_name() {
		return third_name;
	}
	public void setThird_name(String third_name) {
		this.third_name = third_name;
	}
	public String getThird_type() {
		return third_type;
	}
	public void setThird_type(String third_type) {
		this.third_type = third_type;
	}
	public String getMer_no() {
		return mer_no;
	}
	public void setMer_no(String mer_no) {
		this.mer_no = mer_no;
	}
	public String getMer_key() {
		return mer_key;
	}
	public void setMer_key(String mer_key) {
		this.mer_key = mer_key;
	}
	public String getReturn_url() {
		return return_url;
	}
	public void setReturn_url(String return_url) {
		this.return_url = return_url;
	}

	
	
	
}
