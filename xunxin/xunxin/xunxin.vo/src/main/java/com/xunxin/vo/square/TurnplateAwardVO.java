public class TurnplateAwardVO {

	private int 								id;
	private String 								awardLevel;			//奖励等级
	private String 								awardContent;		//奖励内容
	private Date 								createTime;			//创建时间
	private int 								awardState;			//倍数/状态
	
	public TurnplateAwardVO() {
		super();
	}
	
	public TurnplateAwardVO(int id, String awardLevel, String awardContent, Date createTime, int awardState) {
		super();
		this.id = id;
		this.awardLevel = awardLevel;
		this.awardContent = awardContent;
		this.createTime = createTime;
		this.awardState = awardState;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAwardLevel() {
		return awardLevel;
	}
	public void setAwardLevel(String awardLevel) {
		this.awardLevel = awardLevel;
	}
	public String getAwardContent() {
		return awardContent;
	}
	public void setAwardContent(String awardContent) {
		this.awardContent = awardContent;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public int getAwardState() {
		return awardState;
	}
	public void setAwardState(int awardState) {
		this.awardState = awardState;
	}
	
	
	
	
	
	
}
