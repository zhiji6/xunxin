public class UserExpChangeRecord extends GeneralBean{

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1445924997156016358L;
	
	private int 							changeType;			//哪个 场景，例如：转盘游戏，广场答题等  TODO
	private String							direction;			//income|expend
	private int 							tansferBefore;		//订单交易前
	private int 							tansferAmount;		//交易金额
	private int 							tansferEnd;			//订单交易后
	private int 							userId;				//用户
	
	public int getChangeType() {
		return changeType;
	}
	public void setChangeType(int changeType) {
		this.changeType = changeType;
	}
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public int getTansferBefore() {
		return tansferBefore;
	}
	public void setTansferBefore(int tansferBefore) {
		this.tansferBefore = tansferBefore;
	}
	public int getTansferAmount() {
		return tansferAmount;
	}
	public void setTansferAmount(int tansferAmount) {
		this.tansferAmount = tansferAmount;
	}
	public int getTansferEnd() {
		return tansferEnd;
	}
	public void setTansferEnd(int tansferEnd) {
		this.tansferEnd = tansferEnd;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	
	
	
	
	
}
